<p class="tahoma"><span class="txt-m">M</span>ar Toma Margam (Thomas-Christian life-style in India) also has its own emblem or symbol and it is the Mar Toma Sliba (Saint Thomas Cross).&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p class="tahoma">Ancient documents testify that this typical cross was venerated in all the churches and chapels of the Saint Thomas Christians.&nbsp; The original model of it is seen in the chapel on Periyamala, Chennai (Madras).&nbsp; It is a cross, carved on granite stone.</p>
<p class="tahoma-b"><strong>Tradition</strong></p>
<p class="tahoma">According to an ancient local tradition, Saint Thomas the apostle fell dead as a martyr on this particular granite stone.&nbsp; The faithful kept it sacred and at a later stage the typical cross was engraved on it.&nbsp; Modern scholars, after scientific searches, fix the date of engraving somewhere around 650 A. D.&nbsp; The stone, according to them, comes from pre-Christian time. The scholars haven&rsquo;t yet succeeded to decipher conclusively the content of the Sassanian-Pahlavi inscription around the Cross.</p>
<p class="tahoma-b"><strong>In the 16th century</strong></p>
<p class="tahoma">In the 16th century, as the western missionaries were digging the ground to reconstruct the chapel on Periyamala they happened to come across this special Cross.&nbsp; When the construction of the chapel was over, they fixed it to the rear wall of the main altar at the eastern end.&nbsp; It remains there to the present day.</p>
<p class="tahoma-b"><strong>Miracle</strong></p>
<p class="tahoma">On the 18th of December 1558 during the Gospel reading at the Holy Qurbana, this Cross sweated blood and water.&nbsp; According to the testimony of the missionaries, the miracle was repeated for many years on the same date and in the same manner.&nbsp; They informed the Pope, of this miracle and obtained permission from him to celebrate December 18th as the feast of this special Cross&rsquo;s apparition.</p>
<p class="tahoma" style="color: #b30000;">December 18th is a big feast day for the Mar Toma Nazranees.&nbsp; It is the feast of Mar Toma Sliba, the unique Symbol of Mar Toma Margam, their special Christian life-style.&nbsp; They ought to celebrate this feast most solemnly wherever they are.</p>
